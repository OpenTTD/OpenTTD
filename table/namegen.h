enum CzechGender {
	CZG_SMASC,
	CZG_SFEM,
	CZG_SNEUT,
	CZG_PMASC,
	CZG_PFEM,
	CZG_PNEUT,
	// Special for substantive stems - the ending chooses the gender.
	CZG_FREE,
	// Like CZG_FREE, but disallow CZG_SNEUT.
	CZG_NFREE
};
enum CzechPattern {
	CZP_JARNI,
	CZP_MLADY,
	CZP_PRIVL
};
/* [CzechGender][CzechPattern] - replaces the last character of the adjective
 * by this. */
